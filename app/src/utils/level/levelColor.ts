export const getLevelColor = (lvl: Number) => {
  switch (lvl) {
    case 1:
      return {
        aspirant: "Enthusiast",
        color: "#66D9B3",
      };
    case 2:
      return {
        aspirant: "Aspirant",
        color: "#CED864",
      };
    case 3:
      return {
        aspirant: "Local Athlete",
        color: "#F8A74B",
      };
    case 4:
      return {
        aspirant: "Athlete",
        color: "#D74559",
      };
    case 5:
      return {
        aspirant: "Olympian",
        color: "#8E51FF",
      };
    default:
      return {
        aspirant: "Beginner",
        color: "#66D9B3",
      };
  }
};
